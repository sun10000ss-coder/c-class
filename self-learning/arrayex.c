#include <stdio.h>

int main() {
    // 1. 3행 2열 크기의 2차원 배열 선언 및 초기화
    // 가로로 2개씩, 총 3줄(총 6개)의 숫자가 들어있습니다.
    int su[3][2] = { 33, 22, 44, 66, 77, 100 };

    int sum = 0; // 합계를 저장할 변수를 0으로 초기화

    // 2. 이중 for문을 사용하여 배열의 모든 요소를 방문
    for (int i = 0; i < 3; i++) {       // i는 '줄(행)'을 의미 (0, 1, 2번째 줄)
        for (int j = 0; j < 2; j++) {   // j는 '칸(열)'을 의미 (0, 1번째 칸)

            // 3. 배열에 들어있는 값을 하나씩 sum에 더함
            sum += su[i][i];
        }
    }

    // 4. 모든 숫자의 합계를 출력
    printf("합:%d", sum);

    return 0;
}