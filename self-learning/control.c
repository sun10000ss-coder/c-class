#include <stdio.h>

int main() {
    int cnt = 0; // 3의 배수의 개수를 저장할 변수 초기화

    // for문의 조건식이 비어있으므로 무한 루프처럼 동작,
    // i는 1부터 시작해서 매 반복마다 1씩 증가(i++)합니다.
    for (int i = 1;; i++) {

        // 1. 탈출 조건: i가 10보다 커지면
        if (i > 10) {
            break; // 반복문을 즉시 종료하고 나갑니다.
        }

        // 2. 3의 배수 판별: i를 3으로 나눈 나머지(%)가 0이라면
        if (i % 3 == 0) {
            cnt++; // 3의 배수이므로 개수를 1 증가시킵니다.
        }
    }

    // 3. 최종 결과 출력
    // 1~10 사이의 3의 배수는 3, 6, 9이므로 '3'이 출력됩니다.
    printf("3의 배수 개수 :%d\n", cnt);

    return 0;
}